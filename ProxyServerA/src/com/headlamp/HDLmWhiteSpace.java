/* The enum below provides values showing if whitespace is OK or
   not for certain values obtained from JSON. In some cases, it
   OK for a value to be all whitespace. In other cases, it is
   not OK for a value to be all whitespace. */ 
public enum HDLmWhiteSpace {
	NONE,
	WHITESPACEOK,
	WHITESPACENOTOK
}